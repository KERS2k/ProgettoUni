drop table if exists users_db;
drop table if exists videogames_db;
drop table if exists personal_library_db;

CREATE TABLE  users_db (
   id INTEGER PRIMARY KEY AUTOINCREMENT,
   name TEXT NOT NULL,
   surname TEXT NOT NULL,
   email TEXT NOT NULL UNIQUE,
   salt TEXT NOT NULL,
   password TEXT NOT NULL
);

CREATE TABLE videogames_db (
    id INT PRIMARY KEY,
    name VARCHAR(50),
    producer VARCHAR(50),
    genre VARCHAR(50),
    release_date DATE
);

CREATE TABLE personal_library_db (
   id INTEGER PRIMARY KEY AUTOINCREMENT,
   user_id INTEGER NOT NULL,
   game_id INTEGER NOT NULL,
   status TEXT
);


